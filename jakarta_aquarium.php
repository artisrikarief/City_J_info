				<p>Bawah laut Indonesia menyimpan kekayaan dan keindahan yang mengagumkan. Suguhan itu kini hadir dalam sebuah destinasi wisata berkonsep edutainment, Jakarta Aquarium yang berlokasi di lantai LG dan LGM Neo Soho, Jakarta Barat.
					<br>
					Bekerja sama dengan Aquaria dan dikelola Taman Safari Indonesia, Jakarta Aquarium baru saja diresmikan oleh Menteri Kelautan dan Perikanan Susi Pudjiastuti pada Selasa, 16 Oktober 2018. Jakarta Aquarium menjadi rumah lebih dari 600 jenis biota laut, termasuk satwa langka dan asli Indonesia.
