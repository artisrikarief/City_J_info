					"Satwa kita mayoritas 90 persen lebih dari satwa endemik Indonesia, berarti yang ada di Indonesia saja. Satwa endemik kami seperti belalang bambu, belalang daun, sebenarnya dari Jawa Barat bentuknya unik seperti daun, binturong bahasa Inggrisnya bearcat yang badannya kayak kucing, tapi mukanya kayak beruang," kata Hans Manansang selaku Deputy Director PT Taman Safari Indonesia pada Selasa, 16 Oktober 2018.</p>
						<br>
							<p>Namun, ada pula satwa-satwa yang berasal dari luar Indonesia. "Dari luar Indonesia sedikit, ada salamander dari Vietnam dan China Selatan, adanya iguana dari Amerika Tengah," katanya.
					<br>
					Ada beragam atraksi yang dapat dinikmati di Jakarta Aquarium. Mulai dari Touch Pool, pengunjung dapat menyentuh dan mempelajari satwa, 5D Theater-Sea Explorer merasakan keseruan jelajahi dasar laut, Guest Service yaitu berkeliling arena ditemani personal guide, Kids Classroom, Pearl of The South Sea berupa pertunjukan dari cerita rakyat Indonesia, serta Aqua Trekking.
					<br>
					Untuk menyaksikan deretan keseruan di Jakarta Aquarium, tiket dibagi menjadi dua jenis, yakni regular dan premium. Weekday regular harga tiket dewasa Rp 150 ribu dan anak-anak Rp 125 ribu, sedang weekend dewasa Rp 200 ribu dan anak-anak Rp 150 ribu.
					<br>
					Sementara, jenis tiket premium dilengkapi dengan layanan 5D simulator dan multiple entry. Harga tiket weekday premium untuk dewasa Rp 200 ribu dan anak-anak Rp 150 ribu, untuk weekend harga tiket premium dewasa Rp 225 ribu dan anak-anak Rp 175 ribu. Jakarta Aquarium buka Senin - Minggu dan hari libur nasional mulai pukul 10.00-20.00 WIB.</p>
